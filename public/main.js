function set_H_Red()
{
    for (let i = 0; i < document.getElementsByTagName('h2').length; i++)
    {
        document.getElementsByTagName('h2')[i].style.background = "#91121b";
    }
}

function set_H_Blue()
{
    for (let i = 0; i < document.getElementsByTagName('h2').length; i++)
    {
        document.getElementsByTagName('h2')[i].style.background = "rgb(61, 187, 129)";
    }
}